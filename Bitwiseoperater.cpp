// C++ program to demonstrate working of unary increment
// and decrement operators
#include <iostream>
using namespace std;

int main()
{
int a = 36;
int b = 35;
cout<<"BITWISE OPERATOR\n";
cout<<"~a is "<<~a<<endl;
cout<<"a&b is "<<(a&b)<<endl;
cout<<"a|b is "<<(a|b)<<endl;
cout<<"a^b is "<<(a^b)<<endl;
cout<<"~a is "<<~a<<endl;
cout<<"a>> is "<<(a>>1)<<endl;
cout<<"a<< is "<<(a<<1)<<endl;
cout<<"a=b is "<<(a=b)<<endl;
cout<<"~n & b is "<<~a <<endl;


}
